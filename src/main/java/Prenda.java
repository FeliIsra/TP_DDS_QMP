public class Prenda {
    String tela;
    String colorPrimario;
    String colorSecundario;
    Categoria categoria;
    TipoPrenda tipoPrenda;

    Prenda(String _tela, String _colorPrimario, String _colorSecundario, Categoria _categoria, TipoPrenda _tipoPrenda){
        tela = _tela;
        colorPrimario = _colorPrimario;
        colorSecundario = _colorSecundario;
        categoria = _categoria;
        tipoPrenda = _tipoPrenda;
    }

}
